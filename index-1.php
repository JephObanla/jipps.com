&lt;p&gt;The African Journal on Privacy and Data Protection (the &lt;em&gt;Journal&lt;/em&gt;) is domiciled in the Faculty of Law, University of Lagos Akoka-Lagos, Nigeria and published once a year by the Pretoria University Law Press (PULP) in South Africa. The Journal is peer reviewed and open access.&lt;/p&gt;
&lt;p&gt;The &lt;em&gt;Journal&lt;/em&gt; aims to promote African expertise and literature on Africa in the area of privacy and data protection. More specifically, the Journal aims to:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;foster African-centred research and knowledge generation on privacy and data protection;&lt;/li&gt;
&lt;li&gt;fill the critical knowledge gap in this area as well as encourage privacy and data protection discourse from an African perspective;&lt;/li&gt;
&lt;li&gt;facilitate access of African scholars to new and developing knowledge in this area as well as showcase African scholars and perspectives to the world.&lt;/li&gt;
&lt;/ul&gt;
